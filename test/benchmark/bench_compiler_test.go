package benchmark

// TestGenerateBenchmarks (Phase 2 of DESIGN-benchmark.md) emits, for a fixed
// set of queries, paired benchmark funcs into test/tmp: BenchmarkInterp_X runs
// the query via engine.ExecOp over a baked Op tree; BenchmarkCompiled_X runs
// the same query as compiler-generated Go (the operators fused inline). Same
// data, same no-op yield, side by side -- so `go test -bench` directly shows
// the fusion payoff. Run: make bench-compiler

import (
	"fmt"
	"io/ioutil"
	"strings"
	"testing"

	"github.com/couchbase/n1k1/base"
	"github.com/couchbase/n1k1/glue/emit"
)

func benchScanFilterProjectOp(data string, reps int) *base.Op {
	scan := &base.Op{Kind: "scan", Labels: base.Labels{"."},
		Params: []interface{}{"jsonsData", data, reps}}
	filt := &base.Op{Kind: "filter", Labels: base.Labels{"."},
		Params: []interface{}{"eq",
			[]interface{}{"labelPath", ".", "type"},
			[]interface{}{"json", `"contact"`}},
		Children: []*base.Op{scan}}
	return &base.Op{Kind: "project", Labels: base.Labels{`.["g"]`, `.["age"]`},
		Params: []interface{}{
			[]interface{}{"labelPath", ".", "g"},
			[]interface{}{"labelPath", ".", "age"}},
		Children: []*base.Op{filt}}
}

func benchGroupByOp(data string, reps int) *base.Op {
	scan := &base.Op{Kind: "scan", Labels: base.Labels{"."},
		Params: []interface{}{"jsonsData", data, reps}}
	return &base.Op{Kind: "group", Labels: base.Labels{`.["g"]`, "count"},
		Params: []interface{}{
			[]interface{}{[]interface{}{"labelPath", ".", "g"}},
			[]interface{}{[]interface{}{"labelPath", "."}},
			[]interface{}{[]interface{}{"count"}},
		},
		Children: []*base.Op{scan}}
}

func TestGenerateBenchmarks(t *testing.T) {
	// Large rows-per-op so each op runs several seconds: per-op fixed setup
	// (MakeExprFunc / MakeVars / reader creation) is then negligible vs row
	// processing, so the interp-vs-compiled delta reflects the per-row codepath
	// (where fusion lives), not setup noise. 200 distinct "g" values => 200
	// groups (no spill); `reps` amplifies to the row count cheaply.
	const distinct = 200
	const benchRowsPerOp = 30_000_000 // ~5s/op at the engine's ~5-7M rows/s.
	reps := benchRowsPerOp / distinct
	data := GenJSONs(distinct, distinct)
	rowsPerOp := distinct * reps

	queries := []struct {
		name string
		op   *base.Op
	}{
		{"ScanFilterProject", benchScanFilterProjectOp(data, reps)},
		{"GroupBy", benchGroupByOp(data, reps)},
	}

	var body []string
	var allEmitted strings.Builder
	for _, q := range queries {
		opLit, ok := emit.BakeOp(q.op)
		if !ok {
			t.Fatalf("emit.BakeOp %s failed", q.name)
		}
		lines := emit.OpToLines(q.op)
		for _, ln := range lines {
			allEmitted.WriteString(ln)
		}
		body = append(body, benchInterpFunc(q.name, opLit, rowsPerOp)...)
		body = append(body, benchCompiledFunc(q.name, lines, rowsPerOp)...)
	}

	c := []string{
		"//go:build n1ql",
		``,
		"// Code generated by TestGenerateBenchmarks. DO NOT EDIT.",
		``,
		"package tmp",
		``,
		`import "os"`,
		`import "testing"`,
		`import "github.com/couchbase/n1k1/base"`,
		`import "github.com/couchbase/n1k1/engine"`,
		`import "github.com/couchbase/n1k1/glue"`,
	}
	for _, oi := range emit.OptionalImports { // reuse the suite-compiler's import set
		if oi.Path == "os" {
			continue // already always-on above
		}
		if strings.Contains(allEmitted.String(), oi.Qualifier) {
			c = append(c, fmt.Sprintf("import %q", oi.Path))
		}
	}
	c = append(c, ``, "var _ = engine.ExecOp", "var _ = glue.MakeVars",
		"var _ = base.Op{}", ``)
	c = append(c, body...)

	err := ioutil.WriteFile("../tmp/generated_by_bench_test.go",
		[]byte(strings.Join(c, "\n")), 0644)
	if err != nil {
		t.Fatal(err)
	}

	t.Logf("generated %d paired interp/compiled benchmarks", len(queries))
}

// benchInterpFunc emits a benchmark that runs the query via engine.ExecOp over
// the baked Op tree (the interpreter path).
func benchInterpFunc(name, opLit string, rows int) []string {
	return []string{
		"// ------------------------------------------",
		fmt.Sprintf("func BenchmarkInterp_%s(b *testing.B) {", name),
		`  lzTmpDir, lzVars := glue.MakeVars("", "n1k1bench")`,
		`  defer os.RemoveAll(lzTmpDir)`,
		fmt.Sprintf("  op := %s", opLit),
		`  n := 0`,
		`  yieldVals := func(base.Vals) { n++ }`,
		`  yieldErr := func(err error) { if err != nil { b.Fatal(err) } }`,
		`  b.ReportAllocs()`,
		`  b.ResetTimer()`,
		`  for i := 0; i < b.N; i++ {`,
		`    engine.ExecOp(op, lzVars, yieldVals, yieldErr, "", "")`,
		`  }`,
		`  b.StopTimer()`,
		fmt.Sprintf(`  if s := b.Elapsed().Seconds(); s > 0 { b.ReportMetric(float64(%d)*float64(b.N)/s, "rows/s") }`, rows),
		`  _ = n`,
		"}",
		``,
	}
}

// benchCompiledFunc emits a benchmark that runs the compiler-generated Go for
// the query (operators fused inline). The whole compiled block runs inside the
// b.N loop -- which matches the interpreter bench, where engine.ExecOp likewise
// rebuilds its expr funcs / buffers per call; the difference being measured is
// the per-row codepath (fused, no per-op function calls), not setup.
func benchCompiledFunc(name string, lines []string, rows int) []string {
	out := []string{
		"// ------------------------------------------",
		fmt.Sprintf("func BenchmarkCompiled_%s(b *testing.B) {", name),
		`  lzTmpDir, lzVars := glue.MakeVars("", "n1k1bench")`,
		`  defer os.RemoveAll(lzTmpDir)`,
		`  _ = lzVars`,
		`  lzN := 0`,
		`  lzYieldVals := func(lzVals base.Vals) { _ = lzVals; lzN++ }`,
		`  lzYieldErr := func(lzErr error) { if lzErr != nil { b.Fatal(lzErr) } }`,
		`  _ = lzYieldVals`,
		`  _ = lzYieldErr`,
		`  b.ReportAllocs()`,
		`  b.ResetTimer()`,
		`  for lzI := 0; lzI < b.N; lzI++ {`,
	}
	out = append(out, lines...)
	out = append(out,
		`  }`,
		`  b.StopTimer()`,
		fmt.Sprintf(`  if s := b.Elapsed().Seconds(); s > 0 { b.ReportMetric(float64(%d)*float64(b.N)/s, "rows/s") }`, rows),
		`  _ = lzN`,
		"}",
		``)
	return out
}
